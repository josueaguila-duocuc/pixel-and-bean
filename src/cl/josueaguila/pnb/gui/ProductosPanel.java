    private class ProductoTableModel extends javax.swing.table.AbstractTableModel {

    private String[] columnNames = { "ID", "Nombre", "Categor√≠a", "Tipo", "Precio", "Activo" };
    private java.util.List<Producto> datos;

    public ProductoTableModel(java.util.List<Producto> datos) {
        this.datos = datos;
    }

    public void setProductos(java.util.List<Producto> datos) {
        this.datos = datos;
        fireTableDataChanged();
    }

    @Override
    public int getRowCount() {
        return datos.size();
    }

    @Override
    public int getColumnCount() {
        return columnNames.length;
    }

    @Override
    public String getColumnName(int column) {
        return columnNames[column];
    }

    @Override
    public Object getValueAt(int rowIndex, int columnIndex) {
        Producto p = datos.get(rowIndex);
        switch (columnIndex) {
            case 0: return p.getId();
            case 1: return p.getNombre();
            case 2: return p.getCategoria();
            case 3: return p.getTipo();
            case 4: return String.format("$%,.0f", p.getPrecio());
            case 5: return p.isActivo() ? "S√≠" : "No";
            default: return null;
        }
    }

    @Override
    public boolean isCellEditable(int rowIndex, int columnIndex) {
        return false;
    }
}
