    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String texto = txtBuscar.getText().trim().toLowerCase();
        if (texto.isEmpty()) {
            modelo.setProductos(productos); // si implementas setProductos en el modelo
            modelo.fireTableDataChanged();
            return;
        }
        java.util.List<Producto> filtrados = new java.util.ArrayList<>();
        for (Producto p : productos) {
            if (p.getNombre().toLowerCase().contains(texto) ||
                p.getCategoria().toLowerCase().contains(texto) ||
                p.getTipo().toLowerCase().contains(texto)) {
                filtrados.add(p);
            }
        }
        modelo.setProductos(filtrados);
        modelo.fireTableDataChanged();
    }//GEN-LAST:event_btnBuscarActionPerformed

