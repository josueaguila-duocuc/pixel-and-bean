    private void cargarProductoSeleccionado() {
    int fila = tablaProductos.getSelectedRow();
    if (fila == -1) return;
    Producto p = productos.get(fila);
    txtNombre.setText(p.getNombre());
    cmbCategoria.setSelectedItem(p.getCategoria());
    cmbTipo.setSelectedItem(p.getTipo());
    txtPrecio.setText(String.valueOf((long)p.getPrecio()));
    chkActivo.setSelected(p.isActivo());
}
    
    private void limpiarFormulario() {
    txtNombre.setText("");
    txtPrecio.setText("");
    cmbCategoria.setSelectedIndex(0);
    if (cmbTipo.getItemCount()>0) cmbTipo.setSelectedIndex(0);
    chkActivo.setSelected(true);
    tablaProductos.clearSelection();
}
    
    private boolean validarFormulario() {
    String nombre = txtNombre.getText().trim();
    String categoria = (String) cmbCategoria.getSelectedItem();
    String tipo = (String) cmbTipo.getSelectedItem();
    String precioStr = txtPrecio.getText().trim();

    if (nombre.isEmpty()) {
        JOptionPane.showMessageDialog(this, "El nombre no puede estar vac√≠o");
        return false;
    }

    if (categoria == null || categoria.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debe seleccionar una categor√≠a");
        return false;
    }

    if (tipo == null || tipo.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debe seleccionar un tipo");
        return false;
    }

    if (precioStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "El precio no puede estar vac√≠o");
        return false;
    }

    try {
        double precio = Double.parseDouble(precioStr);
        if (precio <= 0) {
            JOptionPane.showMessageDialog(this, "El precio debe ser mayor a 0");
            return false;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "El precio debe ser un n√∫mero v√°lido");
        return false;
    }

    return true;
}
    

